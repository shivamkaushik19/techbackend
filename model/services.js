const mongoose = require('mongoose');

// Define schema for services
const serviceSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  phone: {
    type: String,
    required: true,
  },
  message: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});

const Service =  mongoose.model('Service', serviceSchema);
module.exports = Service;
